package leetcode;
/*
    Данный код является шаблонным для решения алгоритмов.
    Часто используется в различных задачах и помогает упростить код.
 */
public class ListNode {
    int val;
    ListNode next;
    ListNode() {}
    ListNode(int val) { this.val = val; }
    ListNode(int val, ListNode next) { this.val = val; this.next = next; }
}
